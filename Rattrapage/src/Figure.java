public abstract class Figure {
	
    double aire;
    double perimetre;

    public double GetAire() {
        return this.aire;
    }

    public void SetAire(double aire) {
        this.aire = aire;
    }

    public double GetPerimetre() {
        return perimetre;
    }

    public void SetPerimetre(double perimetre) {
        this.perimetre = perimetre;
    }

}